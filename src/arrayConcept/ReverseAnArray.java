package arrayConcept;

public class ReverseAnArray {

	public static void main(String[] args) {
	
	
	int arr[]= new int [] {1,5,7};
	System.out.println("Size of Array is "+arr.length);  //3 , 2

for(int i=arr.length-1;i>=0;i--) {     // Index 3-1 = 2 (Value for 2 index index is 7 , Index 2-1 = 1 (Value of 1st Index is 5))   
System.out.println(arr[i]);
	
	}
	
	}
	
	

}
